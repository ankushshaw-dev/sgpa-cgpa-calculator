export function checkEligibility(
  cgpa: number,
  backlogs: number,
  hasGapYear: boolean
): { eligible: Company[]; notEligible: Company[] } {
  const eligible: Company[] = [];
  const notEligible: Company[] = [];

  companies.forEach((company) => {
    const cgpaOk = cgpa >= company.minCGPA;
    const backlogsOk = backlogs <= company.backlogsAllowed;
    const gapYearOk = company.gapYearAllowed || !hasGapYear;

    if (cgpaOk && backlogsOk && gapYearOk) {
      eligible.push(company);
    } else {
      notEligible.push(company);
    }
  });

  return { eligible, notEligible };
}
